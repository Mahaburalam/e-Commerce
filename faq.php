                        <li>
                            <a>1. Why should you outsource?</a>
                            <p style="text-align: justify;">All businesses have a non-core part of their operations, such as customer care, back-end operations, tedious data entries, etc. Having to invest in building infrastructure and training employees for such tasks can drive up your business costs! Instead, it is always the better decision to hire an outsourcing partner who are expert in this field. For more information, you can read our blog post on the benefits of outsourcing.</p>
                        </li>
                        <li>
                            <a>2. How is quality ensured in my projects?</a>
                            <p style="text-align: justify;">Our employees are extensively trained before they are assigned to any client's work and are always kept under supervision. Furthermore, our Quality Control department checks work being done by all employees. These measures ensure that we provide the best of quality to our clients.</p>
                        </li>
                        <li>
                            <a>3. Is my data secured if I hire GrabSales?</a>
                            <p style="text-align: justify;">Yes. All of our employees work under a supervised environment.They know information only on a need to know basis. Browsing and using non-work related websites and apps are strictly prohibited, as is the use of any USB flash drive, CDs, cell phones, and cameras in the operation rooms. Also, our full office is monitored through a CC camera system. All these strict security measures, ensure that your data is not leaked and is kept secured.</p>
                        </li>
                        <li>
                            <a>4. What is the working schedule of GrabSales?</a>
                            <p style="text-align: justify;">We are open 24/7. Our office staffs generally work in 6 days a week, 8 hours a day schedules. We have the full capability to work your preferred time zone, whether it be UK, USA, Canada, Australia or any other, as long as you let us know beforehand, we can schedule our employees to work at the time you prefer.</p>
                        </li>
                        <li>
                            <a>5. What services do you offer?</a>
                            <p style="text-align: justify;">We offer a wide array of outsourcing solutions. Currently, we specialize in Lead Generation, Email & Linkedin Campaigns,Virtual Assistance, Customer Care Support, Back Office Support, Data Entry, SEO and Digital Marketing. To know more about this, please go to "our services" section.</p>
                        </li>
                        <li>
                            <a>6. How skilled are your employees?</a>
                            <p style="text-align: justify;">All of our employees hold a Bachelors or a Masters degree. They are also very skilled in English communication (writing, reading, speaking and listening), with most being very skilled in spoken communication in English.</p>
                        </li>
                        <li>
                            <a>7. Can I swap my agent/assistant if he/she is not performing well?</a>
                            <p style="text-align: justify;">Although, this is something we rarely see due to our tedious hiring and training process, if you are unhappy with the representative assigned for your work, we will have no problem to swap them with someone who performs better.</p>
                        </li>
                        <li>
                            <a>8. I have a question not listed here.</a>
                            <p style="text-align: justify;">Not an issue, feel free to contact us with any other queries you have!</p>
